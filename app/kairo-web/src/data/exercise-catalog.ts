// Equipment hierarchy: what equipment levels are accessible at each level
const equipmentHierarchy: Record<EquipmentLevel, EquipmentLevel[]> = {
  none: ["none"],
  hotel: ["none", "hotel"],
  dumbbells: ["none", "dumbbells"],
  full_gym: ["none", "hotel", "dumbbells", "full_gym"],
};

export function getExercisesByEquipment(level: EquipmentLevel): Exercise[] {
  const accessible = equipmentHierarchy[level];
  return exercises.filter((e) => accessible.includes(e.equipmentRequired));
}

export function getExercisesByMuscleGroup(group: MuscleGroup): Exercise[] {
  return exercises.filter((e) => e.muscleGroup === group);
}
